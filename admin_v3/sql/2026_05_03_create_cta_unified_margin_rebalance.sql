  `target_base_qty` decimal(20,8) unsigned NOT NULL DEFAULT '0.00000000' COMMENT '目标基础币数量',
  `hedged_base_qty` decimal(20,8) unsigned NOT NULL DEFAULT '0.00000000' COMMENT '已半套基础币数量',

  `last_borrow_amount` decimal(20,8) unsigned NOT NULL DEFAULT '0.00000000' COMMENT '最近借款数量',
  `last_executed_base_qty` decimal(20,8) unsigned NOT NULL DEFAULT '0.00000000' COMMENT '最近成交基础币数量',

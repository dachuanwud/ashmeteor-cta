  `hedge_symbol` varchar(64) NOT NULL DEFAULT '' COMMENT 'U本位半套交易对',
  `hedge_market` varchar(32) NOT NULL DEFAULT 'um' COMMENT '半套市场类型',
  `buy_mode` varchar(32) NOT NULL DEFAULT 'cash' COMMENT '底仓买入模式',
  `margin_side_effect_type` varchar(64) NOT NULL DEFAULT '' COMMENT '统一账户杠杆买入方式',
  `target_quote_usd` decimal(20,4) unsigned NOT NULL DEFAULT '0.0000' COMMENT '目标买入名义USDT',
